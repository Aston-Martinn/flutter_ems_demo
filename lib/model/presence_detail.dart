class PresenceDetail {
  final String date;
  final String day;
  final String month;
  final String year;
  final String checkIn;
  final String checkOut;
  final String status;

  PresenceDetail({
    required this.date,
    required this.day,
    required this.month,
    required this.year,
    required this.checkIn,
    required this.checkOut,
    required this.status
  });
}